def calculate_risk_metrics(price: pd.Series):
    if price.empty:
        return 0.0, 0.0
    returns = price.pct_change().dropna()
    if returns.empty:
        return 0.0, 0.0
    volatility = returns.std() * np.sqrt(TRADING_DAYS)
    drawdown = (price / price.cummax() - 1).min()
    return round(volatility,4), round(abs(drawdown),4)

